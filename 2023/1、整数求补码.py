'''给定一个整数，求他的16位补码。
正整数的补码和该数值的原码（即该数值的二进制形式）相同，例1的原码和补码都是0000000000000001
负数的补码求法是符号位不变，其他位取反，然后结果加1。例-1的原码是1000000000000001，补码是1111111111111111。'''

n = int(input())
if n >= 0:
    n = bin(n)[2:].zfill(16)
    print(n)
else:
    n = abs(n)
    n =list(bin(n)[2:].zfill(15))
    for i in range(len(n)):
        if n[i]=='0':
            n[i]='1'
        else:
            n[i]='0'
    ans = int('1'+''.join(n),2)+1
    print(bin(ans)[2:])